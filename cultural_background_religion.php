                    <h3>Basic information</h3>
                    <p>Shingon Buddhism (真言宗 Shingon-shū) is one of principal schools of buddhism in Japan,
                        dating back to the 8th century. "Shingon" means, directly translated,
                        "true word" and refers to the power of sutras (chanting). As a part of the esoteric Vajrayana traditions
                        originating in India, it incorporates a range of ceremonial gestures and visual representations
                        into its religious practices. Although it has grown to a widely spread monastery tradition and was even promoted
                        to the level of state religion during certain periods of japanese history,
                        it is nontheless closely linked to the mostly hidden traditions of asceticism and esoteric rituals,
                        for example the tradition of "shugendo" (mountain asceticism).</p>


